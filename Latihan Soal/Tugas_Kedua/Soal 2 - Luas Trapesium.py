print("PROGRAM UNTUK MENGHITUNG LUAS TRAPESIUM")
alas_atas = float(input("Masukan nilai alas atas : "))
alas_bawah = float(input("Masukan nilai alas bawah :"))
tinggi = float(input("Masukan nilai tinggi : "))

luas_trapesium = ((alas_atas + alas_bawah) * tinggi) / 2

print("Luas trapesium adalah ", luas_trapesium)